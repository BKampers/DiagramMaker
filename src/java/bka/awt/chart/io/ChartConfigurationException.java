package bka.awt.chart.io;


public class ChartConfigurationException extends Exception {


    public ChartConfigurationException(String message) {
        super(message);
    }


    public ChartConfigurationException(Throwable cause) {
        super(cause);
    }


    public ChartConfigurationException(String message, Throwable cause) {
        super(message, cause);
    }

}
